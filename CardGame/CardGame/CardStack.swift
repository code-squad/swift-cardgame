import Foundation

class CardStack {
    var cards : [Card]
    
    init(cards:[Card]) {
        self.cards = cards
    }
    
    func push(card:Card) {
        self.cards.append(card)
    }
    
    func pop() -> Card? {
        return cards.popLast()
    }
    
    func peek() -> Card? {
        return cards.last
    }
    
    func show() {
        print(self.cards)
    }
}
